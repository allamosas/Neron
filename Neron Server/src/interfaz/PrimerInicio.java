    private void seleccionarIdioma() {
        String[] idiomas = {
            "English",
            "Español",
            "Deustch",
            "中文",
            "Français",
            "русский",
            "Italiano",
            "Тоҷикистон",
            "Zulu"};

        String idioma = (String) JOptionPane.showInputDialog(this, "", 
                ResourceBundle.getBundle("messages.messages").getString("SELECCIONA_IDIOMA"),
                JOptionPane.QUESTION_MESSAGE, null, idiomas, idiomas[0]);
        if(idioma != null) {
            switch (idioma) {
                case "Español":
                    Configuration.crearPropIdioma("es");
                    Locale.setDefault(new Locale("es"));
                    break;
                case "English":
                    Configuration.crearPropIdioma("en");
                    Locale.setDefault(new Locale("en"));
                    break;
                case "Françoise":
                    Configuration.crearPropIdioma("fr");
                    Locale.setDefault(new Locale("fr"));
                    break;
                case "Deustch":
                    Configuration.crearPropIdioma("de");
                    Locale.setDefault(new Locale("de"));
                    break;
                case "русский":
                    Configuration.crearPropIdioma("ru");
                    Locale.setDefault(new Locale("ru"));
                    break;
                case "中文":
                    Configuration.crearPropIdioma("zh");
                    Locale.setDefault(new Locale("zh"));
                    break;
                case "Italiano":
                    Configuration.crearPropIdioma("it");
                    Locale.setDefault(new Locale("it"));
                    break;
                case "Тоҷикистон":
                    Configuration.crearPropIdioma("tg");
                    Locale.setDefault(new Locale("tg"));
                    break;
                case "Zulu":
                    Configuration.crearPropIdioma("zu");
                    Locale.setDefault(new Locale("zu"));
                    break;
            }
        }
    }
