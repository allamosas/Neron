    private void entrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_entrarActionPerformed
        PassManager pm = new PassManager();
        switch (pm.validarComplejidad(this.txtPass, this.txtPassRep)) {
            case -1:
                JOptionPane.showMessageDialog(this,
                        ResourceBundle.getBundle("messages.messages")
                                .getString("NO_COINCIDEN"),
                        ResourceBundle.getBundle("messages.messages")
                                .getString("CONTRASENA_INCORRECTA"),
                        JOptionPane.ERROR_MESSAGE);
                break;
            case 0:
                pm.establecerContrasena(txtPass);
                this.setVisible(false);
                break;
            case 1:
                JOptionPane.showMessageDialog(this,
                        ResourceBundle.getBundle("messages.messages")
                                .getString("DEMASIADO_CORTA"),
                        ResourceBundle.getBundle("messages.messages")
                                .getString("CONTRASENA_INCORRECTA"), 
                        JOptionPane.ERROR_MESSAGE);
                break;
            case 2:
                JOptionPane.showMessageDialog(this,
                        ResourceBundle.getBundle("messages.messages")
                                .getString("ESPACIOS_EN_BLANCO"),
                        ResourceBundle.getBundle("messages.messages")
                                .getString("CONTRASENA_INCORRECTA"), 
                        JOptionPane.ERROR_MESSAGE);
                break;
            case 3:
                JOptionPane.showMessageDialog(this,
                        ResourceBundle.getBundle("messages.messages")
                                .getString("NO_CARACTER_ESPECIAL"),
                        ResourceBundle.getBundle("messages.messages")
                                .getString("CONTRASENA_INCORRECTA"),
                        JOptionPane.ERROR_MESSAGE);
                break;
            case 4:
                JOptionPane.showMessageDialog(this,
                        ResourceBundle.getBundle("messages.messages")
                                .getString("NO_MAYUSCULA"),
                        ResourceBundle.getBundle("messages.messages")
                                .getString("CONTRASENA_INCORRECTA"),
                        JOptionPane.ERROR_MESSAGE);
                break;
            case 5:
                JOptionPane.showMessageDialog(this,
                        ResourceBundle.getBundle("messages.messages")
                                .getString("NO_MINUSCULA"),
                        ResourceBundle.getBundle("messages.messages")
                                .getString("CONTRASENA_INCORRECTA"), 
                        JOptionPane.ERROR_MESSAGE);
                break;
            case 6:
                JOptionPane.showMessageDialog(this,
                        ResourceBundle.getBundle("messages.messages")
                                .getString("NO_DIGITO"),
                        ResourceBundle.getBundle("messages.messages")
                                .getString("CONTRASENA_INCORRECTA"), 
                        JOptionPane.ERROR_MESSAGE);
                break;
            default:
                JOptionPane.showMessageDialog(this,
                        ResourceBundle.getBundle("messages.messages")
                                .getString("ERROR_DESCONOCIDO"),
                        ResourceBundle.getBundle("messages.messages")
                                .getString("CONTRASENA_INCORRECTA"), 
                        JOptionPane.ERROR_MESSAGE);
                break;
        }
    }//GEN-LAST:event_entrarActionPerformed

