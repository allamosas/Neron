    private void cargarDatos() {
        String puerto = Integer.toString(Configuration.getPropPuerto());
        txtPuerto.setText(puerto);
        chkPreguntarAlSalir.setSelected(Configuration.getPropCierre()[0]);
        chkStartup.setSelected(Configuration.getPropCierre()[1]);
        chkPenalizar.setSelected((boolean)Configuration.getPropPenalizacion()[0]);
        txtIntentos.setText(Integer.toString((int)Configuration.getPropPenalizacion()[1]));
        txtTiempoEspera.setText(Integer.toString((int)Configuration.getPropPenalizacion()[2]));
    }

    private void establecerAjustes() {        
        Configuration.crearPropCierre(
                chkPreguntarAlSalir.isSelected(),
                chkStartup.isSelected());
        Configuration.crearPropPenalizacion(
                new String[]{
                    Boolean.toString(chkPenalizar.isSelected()),
                    txtIntentos.getText(),
                    txtTiempoEspera.getText()}
        );
    }

    private boolean validarPuerto() {
        boolean valido = false;
        try {
            int puerto = Integer.parseInt(txtPuerto.getText());
            if (puerto >= 1024 && puerto <= 49151) {
                valido = true;
            }
        } catch (Exception ex) {
            source.Logger.error("AjustesAvanzados", "validarPuerto()", 
                    "Se ha introducido un puerto no válido", ex.getMessage());
        }
        return valido;
    }

    private void javaHelp() {
        try {
            File fichero = new File(Configuration.getJavaHelp());
            URL hsURL = fichero.toURI().toURL();            
            HelpSet helpset = new HelpSet(getClass().getClassLoader(), hsURL);
            
            HelpBroker hb = helpset.createHelpBroker();
            hb.enableHelpKey(this.getContentPane(), "principal", helpset);

            Dimension screenSize = Toolkit.getDefaultToolkit().getScreenSize();            
            Point p = new Point((int)screenSize.getWidth()/4,(int)screenSize.getHeight()/4);
            hb.setLocation(p);
        } catch (MalformedURLException ex) {
            source.Logger.error("AjustesAvanzados", "javaHelp()", 
                    "URL mal formada", ex.getMessage());
        } catch (HelpSetException ex) {
            source.Logger.error("AjustesAvanzados", "javaHelp()", 
                    "Excepcion general", ex.getMessage());
        }
    }
