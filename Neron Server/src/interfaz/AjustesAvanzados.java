    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        if (!txtPuerto.getText().equals(old)) {
            if (validarPuerto()) {
                Configuration.crearPropCierre(chkPreguntarAlSalir.isSelected(),
                        chkStartup.isSelected());
                Configuration.crearPropPuerto(txtPuerto.getText());
                lblPuertoIncorrecto.setVisible(false);
                this.setVisible(false);
            } else {
                lblPuertoIncorrecto.setVisible(true);
            }
        } else {
            establecerAjustes();
            this.setVisible(false);
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

