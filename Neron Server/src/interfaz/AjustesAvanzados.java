    private void chkStartupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chkStartupActionPerformed
        String rutaStartup = "C:\\Users\\%username%\\AppData\\Roaming\\Microsoft\\Windows\\Start Menu\\Programs\\Startup";
        try {
            if(chkStartup.isSelected()) {
                String rutaApp = System.getProperty("user.dir") + "\\Neron.exe";
                String procesoLnk = "mklink \"" + rutaStartup + "\\Neron.lnk\" \"" + rutaApp + "\"";
                ProcessBuilder builder = new ProcessBuilder("cmd", "/c", procesoLnk);
                Process p = builder.start();
                int exitCode = p.waitFor();
                if(exitCode != 0) {                    
                    JOptionPane.showMessageDialog(this,
                            ResourceBundle.getBundle("messages.messages").getString("PERMISOS_INSUFICIENTES"),
                            ResourceBundle.getBundle("messages.messages").getString("NERON"), 
                            JOptionPane.ERROR_MESSAGE);
                    chkStartup.setSelected(false);
                }
            } else {            
                String procesoEliminar = "del \"" + rutaStartup + "\\Neron.lnk\"";
                ProcessBuilder builder = new ProcessBuilder("cmd", "/c", procesoEliminar);
                Process p = builder.start();
                int exitCode = p.waitFor();
                if(exitCode != 0) {                
                    System.out.println("Fallo en el proceso - " + exitCode);
                }
            }
        } catch (IOException ex) {
            source.Logger.error("AjustesAvanzados", "chkStartup()", 
                    "Excepción genérica en la creación de acceso directo", ex.getMessage());
        } catch (InterruptedException ex) {
            source.Logger.error("AjustesAvanzados", "chkStartup()", 
                    "Excepción en la creación de acceso directo", ex.getMessage());
        }
    }//GEN-LAST:event_chkStartupActionPerformed

